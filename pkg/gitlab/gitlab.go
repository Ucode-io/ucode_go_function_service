func GetRepoFilesMap(cfg IntegrationData, ref string) (map[string]bool, error) {
	client, err := gitlab.NewClient(cfg.GitlabIntegrationToken, gitlab.WithBaseURL(cfg.GitlabIntegrationUrl+"/api/v4"))
	if err != nil {
		return nil, fmt.Errorf("failed to create gitlab client: %w", err)
	}

	var (
		filesMap = make(map[string]bool)

		opt = &gitlab.ListTreeOptions{
			Ref:       gitlab.Ptr(ref),
			Recursive: gitlab.Ptr(true),
			ListOptions: gitlab.ListOptions{
				PerPage: 100,
				Page:    1,
			},
		}
	)

	for {
		nodes, resp, err := client.Repositories.ListTree(cfg.GitlabProjectId, opt)
		if err != nil {
			return nil, fmt.Errorf("failed to list tree: %w", err)
		}

		for _, node := range nodes {
			if node.Type == "blob" {
				filesMap[node.Path] = true
			}
		}

		if resp.NextPage == 0 {
			break
		}
		opt.Page = resp.NextPage
	}

	return filesMap, nil
}

