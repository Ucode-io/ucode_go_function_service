func CommitFiles(cfg IntegrationData, branch string, files []*nb.McpProjectFiles) ([]byte, error) {
	if len(files) == 0 {
		return nil, errors.New("no files provided")
	}

	existingFiles, err := GetRepoFilesMap(cfg, branch)
	if err != nil {
		return nil, fmt.Errorf("could not check existing files: %w", err)
	}

	var actions []CommitAction

	for _, file := range files {
		actionType := "create"
		if existingFiles[file.Path] {
			actionType = "update"
		}

		actions = append(actions, CommitAction{
			Action:   actionType,
			FilePath: file.Path,
			Content:  file.Content,
			Encoding: "text",
		})
	}

	commitReq := CommitRequest{
		Branch:        branch,
		CommitMessage: "generated by Fazliddin",
		Actions:       actions,
	}

	url := fmt.Sprintf("%s/api/v4/projects/%d/repository/commits",
		cfg.GitlabIntegrationUrl,
		cfg.GitlabProjectId)

	return DoRequestV1(url, cfg.GitlabIntegrationToken, http.MethodPost, commitReq)
}

// PromoteUGenToMaster syncs the u-gen branch to master by committing all u-gen
// files to master in a single commit. Files that exist on master but not on u-gen
// (and are not skipped CI/asset files) are deleted so master exactly mirrors u-gen.
// templateProjectID is the GitLab ID of the React template repo — its package.json
// and package-lock.json are always restored on master to keep npm ci in sync.
// This is the API-level equivalent of a force push from u-gen to master.
func PromoteUGenToMaster(cfg IntegrationData, gitlabURL, token string, templateProjectID int) error {
	// Step 1: get all files from u-gen with content
	ugenFiles, err := fetchArchiveFiles(gitlabURL, token, cfg.GitlabProjectId, config.UGenBranch)
	if err != nil {
		return fmt.Errorf("failed to read %s branch: %w", config.UGenBranch, err)
	}
	if len(ugenFiles) == 0 {
		return fmt.Errorf("%s branch has no files to promote", config.UGenBranch)
	}

	// Step 2: get file paths currently on master
	masterFiles, err := GetRepoFilesMap(cfg, config.DefaultBranch)
	if err != nil {
		return fmt.Errorf("failed to read %s branch file list: %w", config.DefaultBranch, err)
	}

	// Step 3: build commit actions — create/update u-gen files, delete master-only files
	ugenPaths := make(map[string]bool, len(ugenFiles))
	var actions []CommitAction

	for _, f := range ugenFiles {
		ugenPaths[f.Path] = true
		action := "create"
		if masterFiles[f.Path] {
			action = "update"
		}
		actions = append(actions, CommitAction{
			Action:   action,
			FilePath: f.Path,
			Content:  f.Content,
			Encoding: "text",
		})
	}

	// Delete files present on master but absent from u-gen (skip CI/asset files)
	for path := range masterFiles {
		if !ugenPaths[path] && !shouldSkipFile(path) {
			actions = append(actions, CommitAction{
				Action:   "delete",
				FilePath: path,
			})
		}
	}

	// Step 4: restore package.json and package-lock.json from the React template.
	// This ensures master always has the correct lock file in sync with package.json,
	// regardless of whether a previous push corrupted them with AI-generated versions.
	templateCfg := IntegrationData{
		GitlabIntegrationUrl:   cfg.GitlabIntegrationUrl,
		GitlabIntegrationToken: token,
		GitlabProjectId:        templateProjectID,
	}
	for _, pkgFile := range []string{"package.json", "package-lock.json"} {
		content, fetchErr := getFileContent(templateCfg, pkgFile, config.DefaultBranch)
		if fetchErr != nil {
			fmt.Printf("[PROMOTE] warning: could not fetch %s from template (id=%d): %v\n", pkgFile, templateProjectID, fetchErr)
			continue
		}
		action := "update"
		if !masterFiles[pkgFile] {
			action = "create"
		}
		actions = append(actions, CommitAction{
			Action:   action,
			FilePath: pkgFile,
			Content:  content,
			Encoding: "text",
		})
	}

	commitReq := CommitRequest{
		Branch:        config.DefaultBranch,
		CommitMessage: fmt.Sprintf("promote %s to %s", config.UGenBranch, config.DefaultBranch),
		Actions:       actions,
	}

	apiURL := fmt.Sprintf("%s/api/v4/projects/%d/repository/commits", gitlabURL, cfg.GitlabProjectId)
	result, err := DoRequestV1(apiURL, token, http.MethodPost, commitReq)
	if err != nil {
		return fmt.Errorf("commit request failed: %w", err)
	}

	// Check for GitLab-level errors in the response body.
	// Success responses contain both "id" and "message" (the commit message).
	// Error responses contain only "message" with no "id".
	var resp map[string]any
	if jsonErr := json.Unmarshal(result, &resp); jsonErr == nil {
		if _, hasID := resp["id"]; !hasID {
			if msg, ok := resp["message"]; ok {
				return fmt.Errorf("gitlab commit error: %v", msg)
			}
		}
	}

	return nil
}

// getFileContent fetches the raw content of a single file from a GitLab repo via the Files API.
func getFileContent(cfg IntegrationData, filePath, ref string) (string, error) {
	encoded := strings.ReplaceAll(filePath, "/", "%2F")
	url := fmt.Sprintf("%s/api/v4/projects/%d/repository/files/%s/raw?ref=%s",
		cfg.GitlabIntegrationUrl, cfg.GitlabProjectId, encoded, ref)

	req, err := http.NewRequest(http.MethodGet, url, nil)
	if err != nil {
		return "", err
	}
	req.Header.Set("PRIVATE-TOKEN", cfg.GitlabIntegrationToken)

	client := &http.Client{Timeout: 30 * time.Second}
	resp, err := client.Do(req)
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		body, _ := io.ReadAll(resp.Body)
		return "", fmt.Errorf("status %d: %s", resp.StatusCode, string(body))
	}

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return "", err
	}
	return string(body), nil
}

func GetProjectPath(gitlabURL, token string, projectID int) (string, error) {
	apiURL := fmt.Sprintf("%s/api/v4/projects/%d", gitlabURL, projectID)
	req, err := http.NewRequest(http.MethodGet, apiURL, nil)
	if err != nil {
		return "", err
	}
	req.Header.Set("PRIVATE-TOKEN", token)

	client := &http.Client{Timeout: 10 * time.Second}
	resp, err := client.Do(req)
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		b, _ := io.ReadAll(resp.Body)
		return "", fmt.Errorf("gitlab get project returned %d: %s", resp.StatusCode, string(b))
	}

	var project struct {
		Path string `json:"path"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&project); err != nil {
		return "", err
	}
	if project.Path == "" {
		return "", fmt.Errorf("gitlab project has empty path")
	}
	return project.Path, nil
}

