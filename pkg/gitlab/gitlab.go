func exportProject(exportURL, gitlabToken, exportFilePath string) error {
	req, err := http.NewRequest(http.MethodGet, exportURL, nil)
	if err != nil {
		return fmt.Errorf("failed to create request: %w", err)
	}

	req.Header.Add("Authorization", "Bearer "+gitlabToken)

	client := &http.Client{}

	resp, err := client.Do(req)
	if err != nil {
		return fmt.Errorf("failed to send request: %w", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		return fmt.Errorf("unexpected status code: %d", resp.StatusCode)
	}

	file, err := os.Create(exportFilePath)
	if err != nil {
		return fmt.Errorf("failed to create file: %w", err)
	}
	defer file.Close()

	_, err = io.Copy(file, resp.Body)
	if err != nil {
		return fmt.Errorf("failed to write to file: %w", err)
	}

	return nil
}

func ImportGitLabProject(token, url, namespace, path, filePath string) (github.ImportResponse, error) {
	// Open the file
	file, err := os.Open(filePath)
	if err != nil {
		return github.ImportResponse{}, fmt.Errorf("error opening file: %w", err)
	}
	defer file.Close()

	// Create a buffer to store multipart form data
	body := &bytes.Buffer{}
	writer := multipart.NewWriter(body)

	// Add namespace and path fields
	_ = writer.WriteField("namespace", namespace)
	_ = writer.WriteField("path", path)

	// Add file to the form
	part, err := writer.CreateFormFile("file", filePath)
	if err != nil {
		return github.ImportResponse{}, fmt.Errorf("error creating form file: %w", err)
	}

	// Copy file content to multipart writer
	_, err = io.Copy(part, file)
	if err != nil {
		return github.ImportResponse{}, fmt.Errorf("error copying file content: %w", err)
	}

	// Close the writer to finalize the form
	writer.Close()

	// Create the request
	req, err := http.NewRequest("POST", url, body)
	if err != nil {
		return github.ImportResponse{}, fmt.Errorf("error creating request: %w", err)
	}

	// Set headers
	req.Header.Set("PRIVATE-TOKEN", token)
	req.Header.Set("Content-Type", writer.FormDataContentType())

	// Send request
	client := &http.Client{}
	resp, err := client.Do(req)
	if err != nil {
		return github.ImportResponse{}, fmt.Errorf("error sending request: %w", err)
	}
	defer resp.Body.Close()

	// Check response status
	if resp.StatusCode != http.StatusOK && resp.StatusCode != http.StatusCreated {
		return github.ImportResponse{}, fmt.Errorf("failed to import project, status: %s", resp.Status)
	}

	respByte, err := io.ReadAll(resp.Body)
	if err != nil {
		return github.ImportResponse{}, err
	}

	var importResponse github.ImportResponse
	if err = json.Unmarshal(respByte, &importResponse); err != nil {
		return github.ImportResponse{}, errors.New("failed to unmarshal response body")
	}

	return importResponse, nil
}

// GetRepoCodebase returns all relevant source files from a GitLab repository.
// Always reads from UGenBranch ("u-gen"). If the branch does not exist,
// it is created from DefaultBranch ("master") first.
// Uses the GitLab archive API (single request) instead of per-file requests for speed.
func GetRepoCodebase(gitlabURL, token string, projectID int) ([]RepoFile, error) {
	client, err := gitlab.NewClient(token, gitlab.WithBaseURL(gitlabURL+"/api/v4"))
	if err != nil {
		return nil, fmt.Errorf("failed to create gitlab client: %w", err)
	}

	if err = ensureUGenBranch(client, projectID); err != nil {
		return nil, err
	}

	return fetchArchiveFiles(gitlabURL, token, projectID, config.UGenBranch)
}

// ensureUGenBranch checks if UGenBranch exists; creates it from DefaultBranch if not.
func ensureUGenBranch(client *gitlab.Client, projectID int) error {
	_, _, err := client.Branches.GetBranch(projectID, config.UGenBranch)
	if err == nil {
		return nil
	}

	_, _, err = client.Branches.CreateBranch(projectID, &gitlab.CreateBranchOptions{
		Branch: gitlab.Ptr(config.UGenBranch),
		Ref:    gitlab.Ptr(config.DefaultBranch),
	})
	if err != nil {
		return fmt.Errorf("failed to create branch %q from %q: %w", config.UGenBranch, config.DefaultBranch, err)
	}

	return nil
}

// fetchArchiveFiles downloads the repo as a tar.gz archive (single HTTP request)
// and extracts only the relevant source files, skipping CI/CD configs, OS junk, and binaries.
func fetchArchiveFiles(gitlabURL, token string, projectID int, branch string) ([]RepoFile, error) {
	url := fmt.Sprintf("%s/api/v4/projects/%d/repository/archive.tar.gz?sha=%s", gitlabURL, projectID, branch)

	req, err := http.NewRequest(http.MethodGet, url, nil)
	if err != nil {
		return nil, err
	}
	req.Header.Set("PRIVATE-TOKEN", token)

	httpClient := &http.Client{Timeout: 60 * time.Second}
	resp, err := httpClient.Do(req)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		body, _ := io.ReadAll(resp.Body)
		return nil, fmt.Errorf("gitlab archive request failed with status %d: %s", resp.StatusCode, string(body))
	}

	gzr, err := gzip.NewReader(resp.Body)
	if err != nil {
		return nil, fmt.Errorf("failed to create gzip reader: %w", err)
	}
	defer gzr.Close()

	tr := tar.NewReader(gzr)

	var files []RepoFile
	for {
		header, err := tr.Next()
		if err == io.EOF {
			break
		}
		if err != nil {
			return nil, fmt.Errorf("failed to read tar archive: %w", err)
		}

		if header.Typeflag != tar.TypeReg {
			continue
		}

		// Strip the leading directory added by GitLab (projectname-branch-sha/file.go)
		parts := strings.SplitN(header.Name, "/", 2)
		if len(parts) < 2 || parts[1] == "" {
			continue
		}
		filePath := parts[1]

		if shouldSkipFile(filePath) {
			continue
		}

		content, err := io.ReadAll(tr)
		if err != nil {
			continue
		}

		files = append(files, RepoFile{
			Path:    filePath,
			Content: string(content),
		})
	}

	return files, nil
}

// shouldSkipFile returns true for files that are not useful for frontend preview:
// CI/CD configs, OS metadata, binary assets, and Go dependency lock files.
var (
	skipFileNames = map[string]bool{
		".DS_Store":          true,
		".gitlab-ci.yml":     true,
		"func.yaml":          true,
		"go.sum":             true,
		".gitignore":         true,
		".gitkeep":           true,
		"package.json":       true,
		"package-lock.json":  true,
		"yarn.lock":          true,
		"pnpm-lock.yaml":     true,
		"Dockerfile":         true,
		"Makefile":           true,
		"vite.config.js":     true,
		"vite.config.ts":     true,
		"tsconfig.json":      true,
		"tsconfig.app.json":  true,
		"tsconfig.node.json": true,
	}

	skipDirPrefixes = []string{
		"gitlab/",
		".git/",
		".gitlab/",
	}

	skipExtensions = map[string]bool{
		".png": true, ".jpg": true, ".jpeg": true, ".gif": true,
		".ico": true, ".webp": true, ".bmp": true,
		".woff": true, ".woff2": true, ".ttf": true, ".eot": true, ".otf": true,
		".zip": true, ".tar": true, ".gz": true, ".jar": true,
		".exe": true, ".bin": true, ".dll": true, ".so": true, ".dylib": true,
		".pdf": true, ".doc": true, ".docx": true,
	}
)

func shouldSkipFile(path string) bool {
	return ShouldSkipFile(path)
}

// ShouldSkipFile returns true for files that must not be pushed by AI generation:
// package.json/lock, config files, CI/CD, OS metadata, binaries.
// Exported so handlers can filter before calling CommitFiles.
func ShouldSkipFile(path string) bool {
	base := filepath.Base(path)
	if skipFileNames[base] {
		return true
	}

	for _, prefix := range skipDirPrefixes {
		if strings.HasPrefix(path, prefix) {
			return true
		}
	}

	ext := strings.ToLower(filepath.Ext(path))
	return skipExtensions[ext]
}

func checkExportStatusWithTimeout(baseURL, projectID, exportToken string) error {
	url := fmt.Sprintf("%s/%s/export", baseURL, projectID)
	startTime := time.Now()
	maxWait := 30 * time.Minute

	for {
		if time.Since(startTime) > maxWait {
			return fmt.Errorf("export timed out after %v", maxWait)
		}

		req, _ := http.NewRequest(http.MethodGet, url, nil)
		req.Header.Set("Authorization", "Bearer "+exportToken)

		client := &http.Client{}
		resp, err := client.Do(req)
		if err != nil {
			return err
		}

		var result map[string]string
		json.NewDecoder(resp.Body).Decode(&result)
		resp.Body.Close() // ← явный Close вместо defer внутри цикла

		if result["export_status"] == "finished" {
			return nil
		}

		time.Sleep(10 * time.Second)
	}
}
